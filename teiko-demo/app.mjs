function bindEvents() {
  elements.searchInput?.addEventListener("input", (event) => {
    state.query = event.target.value;
    renderProducts();
  });
  elements.sortSelect.addEventListener("change", (event) => {
    state.sort = event.target.value;
    renderProducts();
  });
  elements.saleOnly.addEventListener("change", (event) => {
    state.saleOnly = event.target.checked;
    renderProducts();
  });
  elements.inStockOnly.addEventListener("change", (event) => {
    state.inStockOnly = event.target.checked;
    renderProducts();
  });
  elements.clearFilters.addEventListener("click", () => {
    state.category = "all";
    state.query = "";
    state.saleOnly = false;
    state.inStockOnly = true;
    state.sort = "popular";
    if (elements.searchInput) elements.searchInput.value = "";
    elements.saleOnly.checked = false;
    elements.inStockOnly.checked = true;
    elements.sortSelect.value = "popular";
    renderAll();
  });
  document.addEventListener("click", (event) => {
    const categoryButton = event.target.closest("[data-category]");
    const addButton = event.target.closest("[data-add]");
    const detailsButton = event.target.closest("[data-details]");
    const favoriteButton = event.target.closest("[data-favorite]");
    const incButton = event.target.closest("[data-inc]");
    const decButton = event.target.closest("[data-dec]");
    const removeButton = event.target.closest("[data-remove]");
    const demoButton = event.target.closest("[data-demo-order]");
    if (categoryButton) selectCategory(categoryButton.dataset.category);
    if (addButton) addToCart(addButton.dataset.add);
    if (detailsButton) openProduct(detailsButton.dataset.details);
    if (favoriteButton) toggleFavorite(favoriteButton.dataset.favorite);
    if (incButton) changeQuantity(incButton.dataset.inc, 1);
    if (decButton) changeQuantity(decButton.dataset.dec, -1);
    if (removeButton) removeFromCart(removeButton.dataset.remove);
    if (demoButton) createDemoOrder();
  });
  elements.cartButton?.addEventListener("click", () => openLayer(elements.cartDrawer));
  elements.closeCart.addEventListener("click", () => closeLayer(elements.cartDrawer));
  elements.openCheckout.addEventListener("click", () => {
    if (!state.cart.length) {
      showToast(t("cartRequired"));
      return;
    }
    openLayer(elements.checkoutModal);
    renderCart();
  });
  elements.closeCheckout.addEventListener("click", () => closeLayer(elements.checkoutModal));
  elements.closeProductModal.addEventListener("click", () => closeLayer(elements.productModal));
  elements.checkoutForm.addEventListener("submit", submitOrder);
  elements.demoOrderButton.addEventListener("click", createDemoOrder);
  elements.catalogButton?.addEventListener("click", () => $("#catalog").scrollIntoView({ behavior: "smooth" }));
  elements.heroPrev.addEventListener("click", () => {
    setHeroSlide(state.heroSlide - 1);
    startHeroCarousel();
  });
  elements.heroNext.addEventListener("click", () => {
    setHeroSlide(state.heroSlide + 1);
    startHeroCarousel();
  });
  elements.heroDots.forEach((dot) => {
    dot.addEventListener("click", () => {
      setHeroSlide(Number(dot.dataset.heroSlide));
      startHeroCarousel();
    });
  });
  elements.heroCarousel.addEventListener("mouseenter", () => window.clearInterval(startHeroCarousel.timer));
  elements.heroCarousel.addEventListener("mouseleave", startHeroCarousel);
  elements.heroCarousel.addEventListener("focusin", () => window.clearInterval(startHeroCarousel.timer));
  elements.heroCarousel.addEventListener("focusout", startHeroCarousel);
  elements.languageToggle?.addEventListener("click", () => {
    state.language = state.language === "ru" ? "en" : "ru";
    localStorage.setItem(storage.language, state.language);
    renderAll();
  });
  [elements.cartDrawer, elements.checkoutModal, elements.productModal].forEach((layer) => {
    layer.addEventListener("click", (event) => {
      if (event.target === layer) closeLayer(layer);
    });
  });
  window.addEventListener("keydown", (event) => {
    if (event.key === "Escape") {
      closeLayer(elements.cartDrawer);
      closeLayer(elements.checkoutModal);
      closeLayer(elements.productModal);
    }
  });
}

