function readJson(key, fallback) {
  try {
    return JSON.parse(localStorage.getItem(key)) || fallback;
  } catch {
    return fallback;
  }
}

function writeJson(key, value) {
  localStorage.setItem(key, JSON.stringify(value));
}

function t(key) {
  return translations[state.language][key] || translations.ru[key] || key;
}

function textOf(value) {
  if (!value || typeof value === "string") return value || "";
  return value[state.language] || value.ru || value.en || "";
}

function money(value) {
  return new Intl.NumberFormat(state.language === "ru" ? "ru-RU" : "en-US", {
    style: "currency",
    currency: "RUB",
    maximumFractionDigits: 0,
  }).format(value);
}

async function loadProducts() {
  try {
    const response = await fetch("data/products.json?v=teiko-carcare-12", { cache: "no-store" });
    if (!response.ok) throw new Error("Catalog unavailable");
    state.products = await response.json();
  } catch {
    state.products = embeddedProducts;
  }
}

function categories() {
  const map = new Map();
  state.products.forEach((product) => map.set(product.category?.ru || product.category?.en, product.category));
  return [{ key: "all", ru: t("all"), en: t("all") }, ...Array.from(map.entries()).map(([key, label]) => ({ key, ...label }))];
}

function filteredProducts() {
  const query = state.query.trim().toLowerCase();
  let products = [...state.products];
  if (state.category !== "all") products = products.filter((product) => (product.category?.ru || product.category?.en) === state.category);
  if (query) {
    products = products.filter((product) =>
      [textOf(product.title), textOf(product.category), textOf(product.description), product.seller, product.sku]
        .join(" ")
        .toLowerCase()
        .includes(query),
    );
  }
  if (state.saleOnly) products = products.filter((product) => product.oldPrice > product.price);
  if (state.inStockOnly) products = products.filter((product) => product.stock > 0);
  products.sort((a, b) => {
    if (state.sort === "priceAsc") return a.price - b.price;
    if (state.sort === "priceDesc") return b.price - a.price;
    if (state.sort === "rating") return b.rating - a.rating;
    return b.reviews + b.rating * 20 - (a.reviews + a.rating * 20);
  });
  return products;
}

function itemQuantity(productId) {
  return state.cart.find((item) => item.id === productId)?.quantity || 0;
}

function cartProducts() {
  return state.cart
    .map((item) => {
      const product = state.products.find((entry) => entry.id === item.id);
      return product ? { ...product, quantity: item.quantity } : null;
    })
    .filter(Boolean);
}

function subtotal() {
  return cartProducts().reduce((sum, product) => sum + product.price * product.quantity, 0);
}

function deliveryCost() {
  const sum = subtotal();
  if (!sum || sum >= 3000) return 0;
  return 299;
}

function total() {
  return subtotal() + deliveryCost();
}

