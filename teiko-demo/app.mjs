function submitOrder(event) {
  event.preventDefault();
  if (!state.cart.length) {
    elements.checkoutStatus.textContent = t("cartRequired");
    return;
  }
  const payload = Object.fromEntries(new FormData(elements.checkoutForm).entries());
  if (!payload.customerName || !payload.phone || !payload.email || !payload.address) {
    elements.checkoutStatus.textContent = t("formRequired");
    return;
  }
  const order = {
    id: `TK-${Date.now().toString().slice(-8)}`,
    createdAt: new Date().toISOString(),
    customer: payload,
    items: cartProducts().map((product) => ({
      id: product.id,
      sku: product.sku,
      title: product.title,
      price: product.price,
      quantity: product.quantity,
    })),
    subtotal: subtotal(),
    delivery: deliveryCost(),
    total: total(),
    paymentStatus: "not_connected_demo",
  };
  const orders = readJson(storage.orders, []);
  orders.push(order);
  writeJson(storage.orders, orders);
  elements.checkoutStatus.textContent = `${t("orderCreated")}. ${t("orderNumber")}: ${order.id}. ${t("savedLocal")}`;
  state.cart = [];
  writeJson(storage.cart, state.cart);
  elements.checkoutForm.reset();
  renderAll();
  showToast(`${t("orderCreated")}: ${order.id}`);
}

