function renderProducts() {
  const products = filteredProducts();
  elements.resultCount.textContent = `${products.length} ${t("result")}`;
  elements.productGrid.innerHTML = products.length
    ? products.map(productCard).join("")
    : `<div class="empty-state"><i data-lucide="search-x"></i><h3>${t("emptyResults")}</h3><p>${t("emptyHint")}</p></div>`;
  refreshIcons();
}

function renderCart() {
  const products = cartProducts();
  const count = state.cart.reduce((sum, item) => sum + item.quantity, 0);
  const sum = subtotal();
  const delivery = deliveryCost();
  if (elements.cartCount) elements.cartCount.textContent = count;
  if (elements.favoriteCount) elements.favoriteCount.textContent = state.favorites.length;
  elements.drawerTitle.textContent = money(total());
  elements.cartSubtotal.textContent = money(sum);
  elements.deliveryPrice.textContent = delivery ? money(delivery) : t("free");
  elements.cartTotal.textContent = money(total());
  elements.openCheckout.disabled = !products.length;

  elements.cartItems.innerHTML = products.length
    ? products
        .map(
          (product) => `
            <div class="cart-item">
              <div class="cart-thumb">${imageTemplate(product)}</div>
              <div>
                <strong>${textOf(product.title)}</strong>
                <span>${money(product.price)}</span>
                <div class="quantity-row">
                  <button type="button" data-dec="${product.id}"><i data-lucide="minus"></i></button>
                  <span>${product.quantity}</span>
                  <button type="button" data-inc="${product.id}"><i data-lucide="plus"></i></button>
                  <button type="button" data-remove="${product.id}" title="${t("remove")}"><i data-lucide="trash-2"></i></button>
                </div>
              </div>
            </div>
          `,
        )
        .join("")
    : `<div class="empty-cart"><i data-lucide="shopping-bag"></i><p>${t("emptyCart")}</p></div>`;

  elements.checkoutItems.innerHTML = products.length
    ? products.map((product) => `<div class="checkout-item"><span>${textOf(product.title)} x ${product.quantity}</span><strong>${money(product.price * product.quantity)}</strong></div>`).join("")
    : `<p class="muted">${t("emptyCart")}</p>`;
  elements.checkoutTotal.textContent = money(total());
  refreshIcons();
}

function renderHeroCarousel() {
  elements.heroSlides.forEach((slide, index) => {
    const active = index === state.heroSlide;
    slide.classList.toggle("active", active);
    slide.setAttribute("aria-hidden", active ? "false" : "true");
  });
  elements.heroDots.forEach((dot, index) => {
    const active = index === state.heroSlide;
    dot.classList.toggle("active", active);
    dot.setAttribute("aria-current", active ? "true" : "false");
  });
}

function setHeroSlide(index) {
  const totalSlides = elements.heroSlides.length;
  if (!totalSlides) return;
  state.heroSlide = (index + totalSlides) % totalSlides;
  renderHeroCarousel();
}

function startHeroCarousel() {
  if (!elements.heroSlides.length) return;
  window.clearInterval(startHeroCarousel.timer);
  startHeroCarousel.timer = window.setInterval(() => setHeroSlide(state.heroSlide + 1), 5200);
}

function renderAll() {
  renderI18n();
  renderHeroCarousel();
  renderCategories();
  renderProducts();
  renderCart();
}

function addToCart(productId, quantity = 1) {
  const product = state.products.find((entry) => entry.id === productId);
  if (!product) return;
  const item = state.cart.find((entry) => entry.id === productId);
  if (item) item.quantity = Math.min(product.stock, item.quantity + quantity);
  else state.cart.push({ id: productId, quantity: Math.min(product.stock, quantity) });
  writeJson(storage.cart, state.cart);
  renderProducts();
  renderCart();
  showToast(t("added"));
}

function changeQuantity(productId, delta) {
  const product = state.products.find((entry) => entry.id === productId);
  const item = state.cart.find((entry) => entry.id === productId);
  if (!product || !item) return;
  item.quantity += delta;
  if (item.quantity <= 0) state.cart = state.cart.filter((entry) => entry.id !== productId);
  else item.quantity = Math.min(product.stock, item.quantity);
  writeJson(storage.cart, state.cart);
  renderProducts();
  renderCart();
}

function removeFromCart(productId) {
  state.cart = state.cart.filter((item) => item.id !== productId);
  writeJson(storage.cart, state.cart);
  renderProducts();
  renderCart();
}

function toggleFavorite(productId) {
  if (state.favorites.includes(productId)) {
    state.favorites = state.favorites.filter((id) => id !== productId);
    showToast(t("favoriteRemoved"));
  } else {
    state.favorites.push(productId);
    showToast(t("favoriteAdded"));
  }
  writeJson(storage.favorites, state.favorites);
  renderProducts();
  renderCart();
}

